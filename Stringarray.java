package com.src;

public class Stringarray {
	
	public static void main(String[] args) {
		// Declaring and instantiating the array
		String a[] = new String[3];
		// initialization
		a[0]="Krishna";
		a[1]="Gopi";
		a[2]="Radha";
		// traversing the array
	for(int i=0;i<a.length;i++) {
		System.out.println(a[i]);
	}
	}
	

}


